/* ALIAS DE COLUNAS */

SELECT NOME AS CLIENTE, SEXO, EMAIL FROM CLIENTE; //PROJECAO

SELECT NOME, SEXO, EMAIL, ENDERECO FROM CLIENTE; //SELECAO

SELECT NOME, SEXO, EMAIL, NOW() AS DATA_HORA FROM CLIENTE; //JUNCAO 

/* APENAS PARA FINS ACADEMICOS */

SELECT * FROM CLIENTE; //TRAZ TODOS OS DADOS DA TABELA

/* FILTRANDO TABELA USANDO WHERE(ONDE) */

SELECT NOME, SEXO FROM CLIENTE 
WHERE SEXO = 'M';

/* UTILIZANDO O LIKE PARA TRAZER APROXIMACOES */

SELECT NOME, SEXO FROM CLIENTE WHERE ENEDERECO LIKE 'LINDO';

/* CARACTER CORINGA % -> QUALQUER COISA */

SELECT NOME, SEXO FROM CLIENTE WHERE ENEDERECO LIKE '%LINDO';

/* CONTANDO REGISTROS DA TABELA */

SELECT COUNT(*) AS "QUANTIDADE DE REGISTROS DA TAB. CLIENTE" FROM CLIENTE;

/* OPERADOR GROUP BY */

SELECT SEXO, COUNT(*)
FROM CLIENTE
GROUP BY SEXO;
















