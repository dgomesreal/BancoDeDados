/* INICIANDO A MODELAGEM FÍSICA */

/* CRIANDO O BANDO DE DADOS */

CREATE DATABASE PROJETO;

/* CONECTAR AO BANCO */

USE PROJETO;

/* CRIANDO A TABELA CLIENTE */

CREATE TABLE CLIENTE(
	NOME VARCHAR(30),
	SEXO CHAR(1),
	EMAIL VARCHAR(30),
	CPF INT(11),
	TELEFONE VARCHAR(30),
	ENEDERECO VARCHAR(100)
); 


/* VERIFICAR TABELAS NO BANCO */

SHOW TABLES;

/* DESCOBRINDO ESTRUTURA DA TABELA */

DESC CLIENTE;

